# Create a Python file called conversion.py

# Pseudocode to solve the problem
# Declare variables
num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"

# Convert variables
int_num1 = int(num1)  # Convert num1 to an integer
float_num2 = float(num2)  # Convert num2 to a float
str_num3 = str(num3)  # Convert num3 to a string
int_string1 = int(string1)  # Convert string1 to an integer

# Print the results
print("Converted variables:")
print("num1 as integer:", int_num1)
print("num2 as float:", float_num2)
print("num3 as string:", str_num3)
print("string1 as integer:", int_string1)
